//Math.round:to round off the value
let a=12.5
console.log(Math.round(a))

//absolute value:it gives the absolute values
b=-123
console.log(Math.abs(b))

//power off:it gives the power of values of the number
console.log(Math.pow(2,10))

//square root:it gives the square root value of a number
console.log(Math.sqrt(545))

//minimum value: to find the minimum value(least value)
a=10,20,30,40,50
console.log(Math.min(a))

//maximum value:to find the maximum valiue(greater value)
console.log(Math.max(100,200,300,1000,1500))

//ceil:it gives its upper integer value
console.log(Math.ceil(9.5))

//floor method:it gives the lower integer value
console.log(Math.floor(8.5))

